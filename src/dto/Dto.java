package dto;



public class Dto {
	  private int id;
	  private int money;
	  private int kubun;
	  private int month;
	  private String buy; 
	
	 
	  public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getMoney() {
		return money;
	}
	public void setMoney(int money) {
		this.money = money;
	}
	public int getKubun() {
		return kubun;
	}
	public void setKubun(int kubun) {
		this.kubun = kubun;
	}
	public int getMonth() {
		return month;
	}
	public void setMonth(int month) {
		this.month = month;
	}
	public String getBuy() {
		return buy;
	}
	public void setBuy(String buy) {
		this.buy = buy;
	}	  
    
   
}

