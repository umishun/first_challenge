package dto;



public class Dto {
	  private int id;
	  private int money;
	  private boolean kubun;
	  private int month;
	  private int year;
	  private int day;
	  private String buy; 
	  
	  public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getMoney() {
		return money;
	}
	public void setMoney(int money) {
		this.money = money;
	}
	public boolean getKubun() {
		return kubun;
	}
	public void setKubun(boolean kubun) {
		this.kubun = kubun;
	}
	public int getMonth() {
		return month;
	}
	public void setMonth(int month) {
		this.month = month;
	}
	public String getBuy() {
		return buy;
	}
	public void setBuy(String buy) {
		this.buy = buy;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public int getDay() {
		return day;
	}
	public void setDay(int day) {
		this.day = day;
	}	  
    
   
}

